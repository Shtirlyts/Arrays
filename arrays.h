/**
 * arrays.h - Заголовочный файл для работы с массивами
 * Функции для работы с одномерными массивами double
 */

#ifndef ARRAYS_H
#define ARRAYS_H

 // ========== ЛАБОРАТОРНАЯ РАБОТА 11 ==========

 /**
  * Ввод элементов массива с клавиатуры
  * @param ptr_array указатель на массив
  * @param n размер массива
  * @return указатель на заполненный массив
  */
double* input_array(double* ptr_array, int n);

/**
 * Печать элементов массива на экран
 * @param ptr_array указатель на массив
 * @param n размер массива
 * @return количество выведенных элементов
 */
int print_array(double* ptr_array, int n);

/**
 * Подсчет элементов массива, значения которых больше заданного числа A
 * @param ptr_array указатель на массив
 * @param n размер массива
 * @param A число для сравнения
 * @return количество элементов, больших A
 */
int count_greater_than_A(double* ptr_array, int n, double A);

// ========== ЛАБОРАТОРНАЯ РАБОТА 14 ==========

/**
 * Заполнение массива значениями функции f(x)
 * @param ptr_array указатель на массив
 * @param n размер массива
 * @return указатель на заполненный массив
 */
double* fill_with_func(double* ptr_array, int n);

/**
 * Вычисление среднего значения элементов, расположенных между минимальным
 * и максимальным значением следующих после него элементов
 * @param arr указатель на массив
 * @param n размер массива
 * @return среднее арифметическое значение элементов между min и max
 */
double average_between_min_max(double* arr, int n);

// ========== ЛАБОРАТОРНАЯ РАБОТА 16 ==========

/**
 * Создание массива d по правилам лабораторной работы 16
 * Генерирует три массива a, b, c случайных размеров (10-50 элементов)
 * с случайными вещественными числами, затем создает массив d:
 * - первый элемент: среднее значение массива b
 * - последний элемент: среднее значение массива c
 * - средние элементы: элементы массива a, начиная с первого положительного,
 *   уменьшенные на 0.5 если >1, увеличенные на 0.5 в противном случае
 * @return указатель на созданный массив d
 */
double* create_array_d();

#endif